package ineuronAssignment1;

public class AverageOfFiveNums {

	public static void main(String[] args) {
		byte a = 10;
		float b = 90.78f;
		int c = 111;
		int d = 8989;
		int e = 7876;
		float avg = (a+b+c+d+e)/5;
		System.out.println("The average of the five numbers is " + avg);
	}

}
